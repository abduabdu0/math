function initSearch(){
  const inp=document.getElementById('searchInp');
  const res=document.getElementById('searchRes');
  if(!inp)return;
  const allTopics=[];
  [SCHOOL,UNI].forEach(nav=>{
    Object.entries(nav).forEach(([grade,data])=>{
      Object.entries(data.top).forEach(([sec,topics])=>{
        topics.forEach(t=>allTopics.push({grade,sec,topic:t}));
      });
    });
  });
  inp.addEventListener('input',()=>{
    const q=inp.value.trim().toLowerCase();
    if(!q){res.classList.remove('open');return;}
    const found=allTopics.filter(x=>x.topic.toLowerCase().includes(q)||x.grade.toLowerCase().includes(q)).slice(0,8);
    if(!found.length){res.classList.remove('open');return;}
    res.innerHTML=found.map(x=>`
      <div class="srch-item" onclick="applySearch('${encodeURIComponent(JSON.stringify(x))}')">
        <b>${x.topic}</b> <span style="color:var(--tx3);font-size:11px">· ${x.grade} · ${x.sec}</span>
      </div>`).join('');
    res.classList.add('open');
  });
  document.addEventListener('click',e=>{if(!inp.contains(e.target))res.classList.remove('open');});
}
function applySearch(encoded){
  const x=JSON.parse(decodeURIComponent(encoded));
  const isSchool=Object.keys(SCHOOL).includes(x.grade);
  ST.lvl=isSchool?'school':'uni';
  ST.grade=x.grade;ST.sec=x.sec;ST.topic=x.topic;
  document.querySelectorAll('.tgl').forEach(b=>{
    b.classList.toggle('on',b.textContent.includes(isSchool?'Школа':'Универ'));
  });
  document.getElementById('gradeLbl').textContent=isSchool?'Класс':'Курс';
  renderGrades();renderSecs();renderTopics();
  document.getElementById('searchRes').classList.remove('open');
  document.getElementById('searchInp').value=x.topic;
  showMsg(`📚 Выбрана тема: ${x.topic}`);
}

