function initDragDrop(){
  const list=document.getElementById('taskList');
  if(!list)return;
  let dragSrc=null;

  list.querySelectorAll('.tcard').forEach(card=>{
    card.addEventListener('dragstart',e=>{
      dragSrc=card;
      card.classList.add('dragging');
      e.dataTransfer.effectAllowed='move';
      e.dataTransfer.setData('text/plain',card.dataset.idx);
    });
    card.addEventListener('dragend',()=>{
      card.classList.remove('dragging');
      list.querySelectorAll('.tcard').forEach(c=>c.classList.remove('drag-over'));
    });
    card.addEventListener('dragover',e=>{
      e.preventDefault();
      e.dataTransfer.dropEffect='move';
      if(card!==dragSrc){
        list.querySelectorAll('.tcard').forEach(c=>c.classList.remove('drag-over'));
        card.classList.add('drag-over');
      }
    });
    card.addEventListener('drop',e=>{
      e.preventDefault();
      if(!dragSrc||dragSrc===card)return;
      const fromIdx=parseInt(dragSrc.dataset.idx);
      const toIdx=parseInt(card.dataset.idx);
      // Переставляем в массиве
      const moved=window._T.splice(fromIdx,1)[0];
      window._T.splice(toIdx,0,moved);
      renderRes(window._T);
      showMsg('↕️ Порядок изменён');
    });
  });
}

