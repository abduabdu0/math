function saveToHistory(){
  if(!window._T||!window._T.length){showErr('Нет задач для сохранения');return;}
  const key='matgen_history';
  const hist=JSON.parse(localStorage.getItem(key)||'[]');
  const entry={
    id:Date.now(),
    topic:ST.topic,
    grade:ST.grade,
    diff:ST.diff,
    cnt:window._T.length,
    tasks:window._T,
    date:new Date().toLocaleDateString('ru-RU')
  };
  hist.unshift(entry);
  if(hist.length>20)hist.pop();
  localStorage.setItem(key,JSON.stringify(hist));
  renderHistory();
  showMsg('💾 Сохранено в историю!');
}
function loadHistory(){
  const key='matgen_history';
  return JSON.parse(localStorage.getItem(key)||'[]');
}
function renderHistory(){
  const hist=loadHistory();
  const el=document.getElementById('histList');
  if(!el)return;
  if(!hist.length){el.innerHTML='<div class="hist-empty">История пуста</div>';return;}
  el.innerHTML=hist.map(h=>`
    <div class="hist-item" onclick="restoreHistory(${h.id})">
      <div class="hist-item-info">
        <div class="hist-item-title">
          ${h.topic}
          ${h.custom?'<span class="custom-badge">своя</span>':''}
          ${h.workCat?`<span style="font-size:10px;color:var(--tx3);margin-left:4px">${workCatLabels[h.workCat]||''}</span>`:''}
        </div>
        <div class="hist-item-meta">${h.grade} · ${h.diff==='easy'?'🟢':h.diff==='med'?'🟡':h.diff==='custom'?'✏️':'🔴'} · ${h.cnt} задач · ${h.date}</div>
        ${h.comment?`<div class="saved-comment">${h.comment}</div>`:''}
      </div>
      <button class="hist-del" onclick="event.stopPropagation();deleteHistory(${h.id})">🗑</button>
    </div>`).join('');
}
function restoreHistory(id){
  const hist=loadHistory();
  const entry=hist.find(h=>h.id===id);
  if(!entry)return;
  window._T=entry.tasks;
  ST.topic=entry.topic;
  renderRes(entry.tasks);
}
function deleteHistory(id){
  const key='matgen_history';
  let hist=JSON.parse(localStorage.getItem(key)||'[]');
  hist=hist.filter(h=>h.id!==id);
  localStorage.setItem(key,JSON.stringify(hist));
  renderHistory();
}
function toggleHistory(){
  const el=document.getElementById('histList');
  if(!el)return;
  const isOpen=el.style.display==='block';
  el.style.display=isOpen?'none':'block';
  document.getElementById('histArrow').textContent=isOpen?'▼':'▲';
  if(!isOpen)renderHistory();
}

